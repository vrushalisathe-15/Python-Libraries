import numpy as np

#Arithmetic operations on array
arr1=np.array([4,5,6])
arr2=np.array([9,8,7])
#ADDITION
print("Addition of an array is" , arr1+arr2)
#SUBTRACTION
print("Subtraction of an array is:", arr1-arr2)

print("Multiplication of an array is:", arr1*arr2)

print("Division of an array is:", arr1/arr2)

print("Modulus of an array is:", arr1%arr2)

print("Exponentiation of an array is:", arr1**arr2)

print("Floor division of an array is:", arr1/arr2)

